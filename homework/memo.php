smarty
Smarty本体が格納されているディレクトリ

templates
テンプレートファイルを格納

templates_c
Smartyでコンパイルされたファイルを保存。コンパイルは自動で行われる(要パーミッション設定:777)

~.php
行いたいPHPの処理をここに書く。~.phpが処理したデータをテンプレートに渡す

~.tpl
表示したい表側(主にHTML)を記述する。PHP側の処理結果を出力する

comn
「.css」「.js」「img」等のファイルを格納。お好みで作成


